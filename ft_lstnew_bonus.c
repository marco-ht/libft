#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*ptr;

	ptr = (t_list *)malloc(sizeof(t_list));
	if (ptr == NULL)
	{
		free(ptr);
		return (NULL);
	}
	ptr->content = content;
	ptr->next = NULL;
	return (ptr);
}
